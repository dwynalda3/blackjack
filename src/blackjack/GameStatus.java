package blackjack;

/**
 * Enum that defines the status of the game.
 */
public enum GameStatus {
	/**
	 * Status that it is player 1's turn.
	 */
	PLAYER1TURN,
	/**
	 * Status that it is player 2's turn.
	 */
	PLAYER2TURN,
	/**
	 * Status that it is player 3's turn.
	 */
	PLAYER3TURN,
	/**
	 * Status that it is the game is over. 
	 */
	FINISHED
	
}
